#!/bin/bash  
cal(){
	return $(expr $1 \* $2)
}
for i in $(seq 1 9);do echo -e "*-*-*-*\t\c";done;echo
for i in $(seq 1 9)  
do   
		for j in $(seq 1 9)
		do
				if [ $i != 5 ]; then 
						cal $i $j
						echo -e "$?\t\c";  
				else 
						echo -e "*-*-*-*\t\c";
				fi
		done
		echo 
done  
for i in $(seq 1 9);do echo -e "*-*-*-*\t\c";done;echo
